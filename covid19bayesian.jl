Let's take a look at the relationship between Râ‚œ, the average number of infection transmissions induced by each infectious person at time t, and the daily reported case totals.

Suppose that the Râ‚œ curve looks like the top graph below. It starts around 2, decreasing over time to about 0.75. The daily case count flattens out around the time Râ‚œ gets to about 1, and then it begins to decrease as Râ‚œ gets smaller than 1.
