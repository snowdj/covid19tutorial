md"### A probabilistic model"

# â•”â•â•¡ 8851d9fc-a9d7-11ea-2ec5-77d5e1799fbd
md"""
Let's think about how we would probablistically simulate the bottom figure given the top figure. We'll make several simplifying assumptions: 

1. Every infectious person is reported at the time they become infectious. This is an oversimplification and should be addressed to obtain a more complete model.
2. Each person is infectious for a specific number of days, which we'll call Î³â»Â¹.

Each day, the number of new reported cases is obtained as a sum of the cases generated by each infectious person over the previous Î³â»Â¹ days. An infectious person infects Î³Râ‚œ persons on average each day, so we can obtain the mean of the number of new cases by multiplying Î³Râ‚œ for each of the previous Î³â»Â¹ days by the corresponding day's number of new infections, and totaling the results.

The mean of this random variable approximately specifies its distribution, because it's approximately binomially distributed with large $n$ and small $p$. In other words, the random variable is approximately Poisson. 

Let's write this up in code. If `cases` is the vector of case counts, we have:

```julia
for i in 2:n
    cases[i] ~ Poisson(max(0, Râ‚œ[i] / Î³â»Â¹ * sum(cases[max(1, i-Î³â»Â¹):i-1])))
end
```
"""

# â•”â•â•¡ 33b1a3d4-a9da-11ea-2042-3bc91380623d
md"""
Remarkably, we can make a few more decisions about the distribution of `Râ‚œ` and `cases` and we have a full-fledged Turing model:
"""

# â•”â•â•¡ a20a2338-a906-11ea-3510-57c76dfec1b1
@model SIR(n, Râ‚œ, cases) = begin
	@assert ismissing(cases) || n == length(cases)
	Î³â»Â¹ = 5
	if ismissing(Râ‚œ)
		Râ‚œ = zeros(Real, n)
	end
	Râ‚œ[1] ~ Uniform(0.5, 3)
	for i in 2:n
		Râ‚œ[i] ~ truncated(Normal(Râ‚œ[i-1], 0.1), 0, Inf)
	end
	if ismissing(cases)
		cases = zeros(Integer, n)
	end
	cases[1] ~ DiscreteUniform(10, 100)
	for i in 2:n
		cases[i] ~ Poisson(max(0, Râ‚œ[i] / Î³â»Â¹ * sum(cases[max(1, i-Î³â»Â¹):i-1])))
	end
	Râ‚œ, cases
end;

