## Bayesian inference

Now suppose we have the case values and want to infer the values of Râ‚œ. The first thing to observe is that the Râ‚œ values are not determined uniquely because of the randomness in the generation of the Poisson random variables. So the conditional distribution of Râ‚œ given `cases` is the best we can hope for.

